package projet;

public class Note {
	private String nom;
	private String projet;
	private String context;
	private String date;
	public String getNom() {
		return nom;
	}
	public String getProjet() {
		return projet;
	}
	public String getContext() {
		return context;
	}
	public String getDate() {
		return date;
	}

}
